# Se pueden utilizar "_" para separar los digitos
n = 100_000_000
print("Separacion de digitos:", n)
print(type(n),"\n")

# Se usa // para division entera
n = 10
r = n // 3
print("Division entera:", r)
print(type(r),"\n")

# Se usa % para obtener el residuo
r = n % 3
print("Residuo:", r)
print(type(r),"\n")

# Se usa ** para elevar a una potencia
r = n ** 3
print("Potencia:", r)
print(type(r),"\n")

# Se usa abs para obtener el valor absoluto
r = abs(-3)
print("Valor absoluto:", r)
print(type(r),"\n")

# Se usa divmod para obtener el cociente y el residuo
r = divmod(10, 3)
print("Cociente y residuo:", r)
print(type(r),"\n")

# Se usa pow para elevar a una potencia
r = pow(10, 3)
print("Potencia:", r)
print(type(r),"\n")

# Se usa round para redondear
r = round(3.14159, 2)
print("Redondeo:", r)
print(type(r),"\n")