# Operadores relacionales
n1 = 10
n2 = 20

# == Operador de igualdad
r = n1 == n2
print("Igualdad:", r)
print(type(r),"\n")

# != Operador de desigualdad
r = n1 != n2
print("Desigualdad:", r)
print(type(r),"\n")

# > Operador mayor que
r = n1 > n2
print("Mayor que:", r)
print(type(r),"\n")

# < Operador menor que
r = n1 < n2
print("Menor que:", r)
print(type(r),"\n")

# >= Operador mayor o igual que
r = n1 >= n2
print("Mayor o igual que:", r)
print(type(r),"\n")

# <= Operador menor o igual que
r = n1 <= n2
print("Menor o igual que:", r)
print(type(r),"\n")
