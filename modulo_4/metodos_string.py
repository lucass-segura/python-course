# Split
# Separa una cadena de texto en una lista
nombres = "Agus, Juan, Pedro, Maria, Luis"
lista_nombres = nombres.split(",") # ['Agus', ' Juan', ' Pedro', ' Maria', ' Luis']
print(lista_nombres)

# Join
# Une una lista de cadenas de texto en una cadena de texto
lista_nombres = ["Agus", "Juan", "Pedro", "Maria", "Luis"]
nombres = ", ".join(lista_nombres) # Agus, Juan, Pedro, Maria, Luis
print(nombres)

